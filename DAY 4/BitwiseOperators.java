class BitwiseOperators{
	public static void main(String[] args) {
		int a=5;
		int b=7;
		int c = 2;
		int d = 3;
		System.out.println("a&b = " + (a&b));
		System.out.println("a|b = " + (a|b));
		System.out.println("a^b = " + (a^b));
		System.out.println("c<<d = " + (c<<d));
		System.out.println("c>>d = " + (c>>d));
	}
}